import java.util.Scanner;

public class Vehicle {
    protected String ID, manufacturer, color, year;
    protected int price;

    public Vehicle(String ID, String manufacturer, String color, String year, int price) {
        this.ID = ID;
        this.manufacturer = manufacturer;
        this.color = color;
        this.year = year;
        this.price = price;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }


}